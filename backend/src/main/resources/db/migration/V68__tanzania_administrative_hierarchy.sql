-- F-settings/NBS: the FULL authoritative Tanzania administrative hierarchy
-- (region -> district -> council -> ward), seeded from the original PMO system's reference CSVs
-- (extracted/maafa.pmo.go.tz/public/adminstrative areas/*.csv). The new platform shipped only a
-- 5-region / 5-district stub and had NO councils level, so Location Management and the public
-- Register-as-Stakeholder cascade saw almost nothing. This adds the councils table + wards.council_id
-- and seeds all four levels: 31 regions, 154 districts, 195 councils (the LGAs people register under),
-- 4081 wards. Idempotent: regions/districts reconcile the existing rows by name (preserving their ids
-- and the incident/oh_event FKs); councils/wards insert-by-code where not already present.
--
-- Linkage is by the source CSV codes (region_code/district_code/council_code/ward_code, all verified
-- unique with 100% parent coverage), NOT the source row ids.

-- 1. councils level (new) + wards.council_id ----------------------------------------------------
create table if not exists public.councils (
    id            bigserial primary key,
    region_id     bigint references public.regions(id),
    district_id   bigint references public.districts(id),
    name          varchar(150) not null,
    council_code  varchar(40) unique,
    region_code   varchar(40),
    district_code varchar(40),
    population     integer,
    is_active     boolean     not null default true,
    created_at    timestamptz not null default now(),
    updated_at    timestamptz not null default now()
);
alter table public.wards add column if not exists council_id bigint references public.councils(id);


