-- System Settings → Translations. The bilingual (English / Kiswahili) UI string registry.
-- Seeded from the public portal's i18n labels (PortalLabels) so the registry mirrors what the
-- site actually renders; admins maintain the EN/SW pairs here. Production-safe additive.
--
-- Honest scope: the live public i18n is still the code-based PortalLabels service; this table is
-- the managed source of truth a future loader can hydrate from (the read endpoint already serves it).
create table if not exists public.translations (
    id          bigint generated by default as identity primary key,
    label_key   varchar(120) not null unique,   -- e.g. 'lbl_home'
    group_name  varchar(60)  not null default 'General',
    en          text not null,
    sw          text not null,
    created_at  timestamptz not null default now(),
    updated_at  timestamptz not null default now()
);
create index if not exists idx_translations_group on public.translations(group_name);

